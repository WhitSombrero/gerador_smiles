def gerar_combinacoes_otimizado(df100, df2, pontes_dict, n_total=10_000, batch=200_000):
    # Copias para segurança
    df = df.copy()
    df2 = df.copy()

    # ============================
    # 1) Filtra apenas moléculas válidas
    # ============================
    df["mol"] = df["smiles"].apply(Chem.MolFromSmiles)
    df = df[df["mol"].notna()]

    df2["mol"] = df2["smiles"].apply(Chem.MolFromSmiles)
    df2 = df2[df2["mol"].notna()]

    smiles1 = df["smiles"].to_numpy(dtype=object)
    smiles2_raw = df2["smiles"].to_numpy(dtype=object)

    # ============================
    # 2) Pré-conversão leve: troca '1' → '2' no segundo DF
    # ============================
    # Usa np.vectorize (mais leve que list comprehension para grandes arrays)
    trocar = np.vectorize(lambda s: s.replace("1", "2"))
    smiles2_mod = trocar(smiles2_raw)

    # ============================
    # 3) Pontes como array
    # ============================
    pontes = np.array([v[0] for v in pontes_dict.values()], dtype=object)

    n1 = len(smiles1)
    n2 = len(smiles2_mod)
    npontes = len(pontes)

    seen = set()
    resultados = []

    # ============================
    # 4) Loop principal (mínimo overhead)
    # ============================
    while len(seen) < n_total:

        # ----------------------------
        # GERAÇÃO TOTALMENTE VETORIZADA
        # ----------------------------
        idx1 = np.random.randint(0, n1, batch)
        idx2 = np.random.randint(0, n2, batch)
        idxp = np.random.randint(0, npontes, batch)

        # concatenação 100% numpy (rápida!)
        candidatos = smiles1[idx1] + pontes[idxp] + smiles2_mod[idx2]

        # ----------------------------
        # VALIDAÇÃO RDKit
        # ----------------------------
        for s in candidatos:
            mol = Chem.MolFromSmiles(s)
            if mol is None:
                continue

            can = Chem.MolToSmiles(mol, canonical=False)

            if can not in seen:
                seen.add(can)
                resultados.append(can)

                if len(seen) >= n_total:
                    break

    return pd.DataFrame({"smiles": resultados})

