from rdkit import Chem
import pandas as pd

def remover_duplicatas_rdkit(df: pd.DataFrame, coluna_smiles: str = "smiles") -> pd.DataFrame:
    """
    Remove duplicatas de moléculas equivalentes no DataFrame usando RDKit.

    Parâmetros:
    - df: DataFrame com uma coluna de SMILES
    - coluna_smiles: nome da coluna que contém os SMILES

    Retorna:
    - DataFrame sem moléculas duplicadas
    """
    seen_mols = set()
    indices_para_manter = []

    for idx, smi in df[coluna_smiles].items():
        mol = Chem.MolFromSmiles(smi)
        if mol is None:
            continue  # ignora SMILES inválidos
        # Canonical SMILES para identificação única
        can_smi = Chem.MolToSmiles(mol, canonical=True)
        if can_smi not in seen_mols:
            seen_mols.add(can_smi)
            indices_para_manter.append(idx)

    # Retorna apenas as linhas com moléculas únicas
    return df.loc[indices_para_manter].reset_index(drop=True)

