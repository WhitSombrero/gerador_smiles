from rdkit import Chem
import pandas as pd

def aromatizar_aneis_seis_membros(smiles):
    """
    Tenta aromatizar todos os anéis de 6 membros.
    Retorna o SMILES aromatizado ou None se falhar.
    """
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return None

    # Identifica anéis
    ri = mol.GetRingInfo()
    atom_rings = ri.AtomRings()

    # Para cada anel, se tiver 6 membros, tenta aromatizar
    for ring in atom_rings:
        if len(ring) == 6:
            # Tenta marcar ligações como aromáticas
            for i in range(len(ring)):
                a1 = ring[i]
                a2 = ring[(i + 1) % len(ring)]
                bond = mol.GetBondBetweenAtoms(a1, a2)
                if bond is None:
                    return None
                bond.SetBondType(Chem.rdchem.BondType.AROMATIC)

            # Marca átomos como aromáticos
            for idx in ring:
                atom = mol.GetAtomWithIdx(idx)
                atom.SetIsAromatic(True)

    # Sanitiza e verifica se é válido
    try:
        Chem.SanitizeMol(mol)
    except:
        return None

    return Chem.MolToSmiles(mol)

