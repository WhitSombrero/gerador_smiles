"""# Função para fundir anéis com auxocromos
 ## posicoes = random.sample(posicoes_possiveis, n) define o número de auxocromos
"""

import random

# ------------------------------------------------------------
# Função para inserir um fragmento em uma posição específica do SMILES
# ------------------------------------------------------------
def inserir_fragmento(smiles: str, pos: int, frag: str) -> str:
    """
    Insere o fragmento frag na posição pos do SMILES smiles.
    """
    pos = max(0, min(pos, len(smiles)))  # garante que a posição seja válida
    return smiles[:pos] + f"({frag})" + smiles[pos:]

# ------------------------------------------------------------
# Função para gerar um SMILES modificado com 3 fragmentos aleatórios
# ------------------------------------------------------------
def gerar_smiles_modificado(smiles_base: str, frags_dict: dict) -> str:
    """
    Gera um SMILES a partir do smiles_base inserindo 3 fragmentos aleatórios
    em posições distintas.
    """
    smiles_mod = smiles_base
    posicoes_possiveis = [2, 3, 4, 5, 6] #### Define a posição dos Auxocromos
    quantia_possivel = 3 #Número de Substituintes

    # Escolhe 3 posições distintas
    posicoes = random.sample(posicoes_possiveis, quantia_possivel) #### Define a quantidade de auxocromos por anel

    # Prepara lista de inserções (posição, fragmento)
    insercoes = [(pos, frags_dict[random.randint(1, 1)]) for pos in posicoes]

    # Ordena em ordem decrescente para não deslocar posições
    insercoes.sort(key=lambda x: x[0], reverse=True)

    # Insere os fragmentos
    for pos, frag in insercoes:
        smiles_mod = inserir_fragmento(smiles_mod, pos, frag)

    return smiles_mod

# ------------------------------------------------------------
# Função para gerar múltiplos SMILES modificados para todos os anéis
# ------------------------------------------------------------
def gerar_todos(aneis_dict: dict, frags_dict: dict, repeticoes: int = 10) -> dict:
    """
    Para cada anel no dicionário aneis_dict, gera repeticoes SMILES modificados.
    """
    resultado_final = {}
    counter = 1

    for key_anel, smiles_base in aneis_dict.items():
        for _ in range(repeticoes):
            smiles_gerado = gerar_smiles_modificado(smiles_base, frags_dict)
            resultado_final[counter] = smiles_gerado
            counter += 1

    return resultado_final

# ------------------------------------------------------------
# Função auxiliar que insere fragmentos seguindo instruções específicas
# ------------------------------------------------------------
def inserir_fragmentos_dict(dict_aneis: dict, dict_frags: dict, key_anel: int, instrucoes: list) -> str:
    """
    Insere fragmentos em um SMILES base seguindo instruções.

    instrucoes: lista de tuplas (posição, key_fragmento)
    """
    base = dict_aneis[key_anel]
    resultado = base
    deslocamento = 0

    # Ordena instruções por posição
    for pos, frag_key in sorted(instrucoes, key=lambda x: x[0]):
        frag = dict_frags[frag_key]
        inserir = f"({frag})"
        pos_real = pos + deslocamento
        resultado = resultado[:pos_real + 1] + inserir + resultado[pos_real + 1:]
        deslocamento += len(inserir)

    return resultado

